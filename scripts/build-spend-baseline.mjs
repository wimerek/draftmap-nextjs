/**
 * scripts/build-spend-baseline.mjs
 *
 * Piece 1 — Spend baseline (one-time generator).
 *
 * For each chart position, computes the average number of drafted players per
 * class and the average first-two-round count, across the 2018–2025 classes,
 * reading the same public Google Sheet the app uses (lib/sheets.ts).
 *
 * Output: data/position_spend_baseline.json (checked into the repo).
 * The crawlable-twin position pages read this STATIC file at render time —
 * they never fetch historical classes.
 *
 * Run:  node scripts/build-spend-baseline.mjs
 * Needs SHEETS_SPREADSHEET_ID in .env.local (same var the app uses).
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

// ── Config ──────────────────────────────────────────────────────────────────
const YEARS = [2018, 2019, 2020, 2021, 2022, 2023, 2024, 2025];
const POSITIONS = ['EDGE', 'DT', 'LB', 'CB', 'S', 'RB', 'WR', 'TE', 'OT', 'IOL', 'QB'];

// ── Read SHEETS_SPREADSHEET_ID from .env.local ───────────────────────────────
function readSpreadsheetId() {
  if (process.env.SHEETS_SPREADSHEET_ID) return process.env.SHEETS_SPREADSHEET_ID;
  const env = readFileSync(join(ROOT, '.env.local'), 'utf8');
  for (const line of env.split('\n')) {
    const m = line.match(/^\s*SHEETS_SPREADSHEET_ID\s*=\s*(.+)\s*$/);
    if (m) return m[1].trim();
  }
  throw new Error('SHEETS_SPREADSHEET_ID not found in env or .env.local');
}

// ── Minimal RFC-4180 CSV parser (mirrors lib/sheets.ts) ──────────────────────
function parseCSVLine(line) {
  const out = [];
  let cur = '', q = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (c === '"') {
      if (q && line[i + 1] === '"') { cur += '"'; i++; }
      else q = !q;
    } else if (c === ',' && !q) { out.push(cur.trim()); cur = ''; }
    else cur += c;
  }
  out.push(cur.trim());
  return out;
}

function parseCSV(text) {
  const lines = text.split('\n').map(l => l.replace(/\r$/, '')).filter(l => l.trim().length > 0);
  if (lines.length < 2) return [];
  const headers = parseCSVLine(lines[0]);
  return lines.slice(1).map(line => {
    const vals = parseCSVLine(line);
    const row = {};
    headers.forEach((h, j) => { row[h] = vals[j] ?? ''; });
    return row;
  });
}

const toInt = v => {
  if (v == null || String(v).trim() === '') return null;
  const n = Number(String(v).trim());
  return isNaN(n) ? null : Math.round(n);
};

// ── Fetch + aggregate ────────────────────────────────────────────────────────
async function main() {
  const id = readSpreadsheetId();
  const url = `https://docs.google.com/spreadsheets/d/${id}/gviz/tq?tqx=out:csv&sheet=players`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Sheets fetch failed: ${res.status} ${res.statusText}`);
  const rows = parseCSV(await res.text());

  // Per-year, per-position tallies
  const tally = {}; // pos -> { drafted: {year:count}, firstTwo: {year:count} }
  for (const pos of POSITIONS) tally[pos] = { drafted: {}, firstTwo: {} };
  for (const pos of POSITIONS) for (const y of YEARS) { tally[pos].drafted[y] = 0; tally[pos].firstTwo[y] = 0; }

  for (const row of rows) {
    const year = toInt(row.draft_year);
    if (!YEARS.includes(year)) continue;

    // Normalize position the same way the data layer does (DL → DT)
    const rawPos = (row.pos ?? '').trim().toUpperCase();
    const pos = rawPos === 'DL' ? 'DT' : rawPos;
    if (!POSITIONS.includes(pos)) continue;

    const rd = toInt(row.rd_drafted);
    const pick = toInt(row.pick_drafted);
    // "Drafted" = an actual draft pick was spent. A player with only team_drafted
    // (and no pick) is a UDFA signing, NOT a drafted player — exclude from spend.
    if (pick === null) continue;

    tally[pos].drafted[year]++;
    if (rd !== null && rd <= 2) tally[pos].firstTwo[year]++;
  }

  const round1 = n => Math.round(n * 10) / 10;
  const positions = {};
  for (const pos of POSITIONS) {
    const draftedCounts = YEARS.map(y => tally[pos].drafted[y]);
    const firstTwoCounts = YEARS.map(y => tally[pos].firstTwo[y]);
    const avg = arr => arr.reduce((a, b) => a + b, 0) / arr.length;
    positions[pos] = {
      avgDrafted: round1(avg(draftedCounts)),
      avgFirstTwoRounds: round1(avg(firstTwoCounts)),
      perYearDrafted: Object.fromEntries(YEARS.map((y, i) => [y, draftedCounts[i]])),
    };
  }

  const output = {
    _comment: 'Generated by scripts/build-spend-baseline.mjs — do not edit by hand. Avg drafted players & first-two-round counts per position across the 2018–2025 classes.',
    seasonsCovered: `${YEARS[0]}-${YEARS[YEARS.length - 1]}`,
    classCount: YEARS.length,
    positions,
  };

  mkdirSync(join(ROOT, 'data'), { recursive: true });
  const outPath = join(ROOT, 'data', 'position_spend_baseline.json');
  writeFileSync(outPath, JSON.stringify(output, null, 2) + '\n', 'utf8');

  console.log(`Wrote ${outPath}`);
  for (const pos of POSITIONS) {
    console.log(`  ${pos.padEnd(4)} avgDrafted=${positions[pos].avgDrafted}  avgFirst2=${positions[pos].avgFirstTwoRounds}`);
  }
}

main().catch(e => { console.error(e); process.exit(1); });
